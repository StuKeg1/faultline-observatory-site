/**
 * generate-redirects.js — writes public/_redirects from the canonical
 * route manifest (scripts/route-manifest.js). It also writes the edge-safe
 * route table consumed by functions/[[path]].js. Cloudflare has not reliably
 * applied exact 200 rewrites on this project, so the Function is the
 * authoritative runtime router and _redirects remains a declarative fallback.
 *
 * Replaces the old blanket `/* /index.html 200` SPA fallback, which
 * rewrote every path (including nonexistent ones) to 200, producing soft
 * 404s. Instead: an exact `200` rewrite per known route, a 301 from each
 * route's non-canonical slash form to its canonical form, and an explicit
 * legacy prog-mf redirect. Anything not listed here falls through to
 * Cloudflare Pages' native 404 handling (public/404.html), since no
 * top-level 404.html + no matching rule disables the automatic SPA
 * fallback.
 *
 * Deliberately excludes /the-record/* — functions/the-record/[[recordId]].js
 * owns that subtree's routing internally (see route-manifest.js) because a
 * Function's route match takes precedence over _redirects for the same
 * path, so rules here for that prefix would not reliably apply.
 *
 * Runs as part of `npm run build` (prebuild) so it can never go stale
 * relative to the corpus/notes/events data it's generated from. Fails the
 * build (non-zero exit) if the manifest or the written file don't check
 * out structurally — this is the routing build gate.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { STATIC_ROUTES, LEGACY_REDIRECTS, getRedirectsManagedRoutes } from "./route-manifest.js";

const ROOT = path.dirname(fileURLToPath(import.meta.url)).replace(/scripts$/, "");
const OUT_PATH = path.join(ROOT, "public", "_redirects");
const FUNCTION_ROUTES_PATH = path.join(ROOT, "functions", "generated-routes.js");

function fail(message) {
  console.error(`Redirects generation failed: ${message}`);
  process.exitCode = 1;
}

function nonCanonicalForm(route) {
  // Routes ending in "/" (the trailing-slash-canonical majority): the
  // non-slash form is non-canonical. Routes that deliberately canonicalize
  // WITHOUT a trailing slash (currently only /events/:eventId): the
  // slash-appended form is non-canonical.
  if (route === "/") return null;
  return route.endsWith("/") ? route.slice(0, -1) : `${route}/`;
}

function buildRedirects(canonicalRoutes) {
  const seen = new Set();
  for (const route of canonicalRoutes) {
    if (seen.has(route)) fail(`duplicate canonical route in manifest: ${route}`);
    seen.add(route);
  }

  const rewriteLines = canonicalRoutes
    .filter((route) => route !== "/")
    .map((route) => `${route} /index.html 200`);

  const slashRedirectLines = canonicalRoutes
    .map((route) => {
      const nonCanonical = nonCanonicalForm(route);
      return nonCanonical ? `${nonCanonical} ${route} 301` : null;
    })
    .filter(Boolean);

  const legacyLines = LEGACY_REDIRECTS.map((r) => `${r.from} ${r.to} 301`);

  return [
    "# Generated by scripts/generate-redirects.js — do not hand-edit.",
    "# Source of truth: scripts/route-manifest.js + corpus/notes/events data.",
    "",
    "# Canonical slash-form redirects (exact matches; ordered ahead of the",
    "# placeholder-based legacy rules below per Cloudflare's own guidance —",
    "# exact rules resolve faster than splat/placeholder rules)",
    ...slashRedirectLines,
    "",
    "# Known routes -> SPA shell (exact matches)",
    ...rewriteLines,
    "",
    "# Legacy id migrations (Release 006) — placeholder rules, kept last",
    ...legacyLines,
    "",
  ].join("\n");
}

function validateWrittenFile(contents, canonicalRoutes) {
  if (/^\s*\/\*\s/m.test(contents) || /^\/\*\s/.test(contents)) {
    fail("generated _redirects still contains a blanket wildcard rule");
  }

  for (const route of canonicalRoutes) {
    if (route === "/") continue;
    if (!contents.includes(`${route} /index.html 200`)) {
      fail(`generated _redirects is missing an exact rule for ${route}`);
    }
  }

  for (const r of LEGACY_REDIRECTS) {
    if (!contents.includes(`${r.from} ${r.to} 301`)) {
      fail(`generated _redirects is missing legacy redirect ${r.from} -> ${r.to}`);
    }
  }
}

function buildFunctionRoutes(canonicalRoutes) {
  return [
    "/**",
    " * Generated by scripts/generate-redirects.js — do not hand-edit.",
    " * Edge-safe route data for functions/[[path]].js.",
    " */",
    `export const CANONICAL_ROUTES = ${JSON.stringify(canonicalRoutes, null, 2)};`,
    `export const LEGACY_REDIRECTS = ${JSON.stringify(LEGACY_REDIRECTS, null, 2)};`,
    "",
  ].join("\n");
}

const canonicalRoutes = await getRedirectsManagedRoutes();
const expectedStaticCount = STATIC_ROUTES.filter((route) => !route.startsWith("/the-record/")).length;

if (canonicalRoutes.length < expectedStaticCount) {
  fail("redirects-managed route set resolved fewer routes than the static list alone — dynamic import likely failed");
}

if (canonicalRoutes.some((route) => route.startsWith("/the-record/"))) {
  fail("a /the-record/* route leaked into the _redirects-managed set — that subtree must stay Function-owned");
}

const contents = buildRedirects(canonicalRoutes);
fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, contents, "utf8");
fs.mkdirSync(path.dirname(FUNCTION_ROUTES_PATH), { recursive: true });
fs.writeFileSync(FUNCTION_ROUTES_PATH, buildFunctionRoutes(canonicalRoutes), "utf8");

validateWrittenFile(fs.readFileSync(OUT_PATH, "utf8"), canonicalRoutes);

if (process.exitCode) {
  process.exit();
}

console.log(`Routes generated (${canonicalRoutes.length} routes, ${LEGACY_REDIRECTS.length} legacy rules) -> public/_redirects + functions/generated-routes.js`);
